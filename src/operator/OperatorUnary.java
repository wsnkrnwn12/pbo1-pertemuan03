package operator;

public class OperatorUnary {
    public static void main(String[] args) {
        int i = 10;
        i++;
        System.out.println(i);
        i++;
        System.out.println(i);
        i=i+1;
        System.out.println(i);
        i*=5;
        System.out.println(i);
        i*=10;
        System.out.println(i);
        i+=20;
        System.out.println(i);
    }
}
