package percabangan;

public class Switch {
    public static void main(String[] args) {
        int Enemy=5;
        switch(Enemy){
            case 1:
                System.out.println("Crocodile");
                break;
            case 2:
                System.out.println("RobLucci");
                break;
            case 3:
                System.out.println("GeckoMoria");
                break;
            case 4:
                System.out.println("CaesarClown");
                break;
            case 5:
                System.out.println("Doflamingo");
                break;
            default:
                System.out.println("Katakuri");
                break;
        }
    }
}
